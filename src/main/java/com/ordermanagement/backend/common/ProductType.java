package com.ordermanagement.backend.common;

public enum ProductType {
    /**
     * A single component or item used in manufacturing, such as a bolt or gear.
     */
    PART,

    /**
     * A product formed by combining multiple parts, such as a motor.
     */
    ASSEMBLY,

    /**
     * Raw material used in production, such as steel or plastic.
     */
    MATERIAL
}
